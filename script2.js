function renderCards(start, end, container) {
    for (let i = start; i <= end; i++) {
        const img = document.createElement('img')
        img.className = "card"
        img.id = `card-${i}`
        img.src = `./src/assets/card-${i}.jpg`

        img.addEventListener('click', () => {
            const cardData = tarotCards.find(c => c.id === i)
            if (cardData) openModal(cardData)
        })

        container.appendChild(img)
    }
}
